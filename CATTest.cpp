// Write a C++ program that declares variables of all basic data types, initializes them, and prints their values.
#include <iostream>
using namespace std;
int main()
{
    // Declare and initialize variables of all basic data types
    int integer = 10;
    char character = 'A';
    float floatingPoint = 3.14;
    double doublePrecision = 3.14159;
    bool boolean = true;
    // Print the values of the variables
    cout << "Integer: " << integer << endl;
    cout << "Character: " << character << endl;
    cout << "Floating Point: " << floatingPoint << endl;
    cout << "Double Precision: " << doublePrecision << endl;
    cout << "Boolean: " << boolean << endl;
    return 0;
}
