#include <iostream>
#include <cmath>//The maths library
using namespace std;

int main()
{
    int x = -10;
    cout << "Absolute value of " << x << " is " << abs(x) << endl;
    cout << "The squareroot of 25 =" << sqrt(25) << endl;
    cout << "The log of 5 base 10 =" << log(5) << endl;
    cout << "2^3 =" << pow(2, 3) << endl;
}
